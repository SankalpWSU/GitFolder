import Foundation
struct Dictionary: Codable {
    let hits: [Pictures]
}

struct Pictures: Codable {
    let image: String
    let likes: Int
    let views: Int
    let comments: Int
    let largeImage: String

    enum CodingKeys: String, CodingKey {
            case image = "previewURL"
            case likes
            case views
            case comments
            case largeImage = "largeImageURL"
        }
}
